const fs = require('fs')
const path = require('path')

const rootDir = path.resolve(__dirname, '..')
const tokenDir = path.join(rootDir, 'src', 'styles', 'tokens')
const outputPath = path.join(rootDir, 'src', 'styles', 'generated-tokens.css')

const rootFiles = [
  'color-core-hex.css',
  'xbase-mode-1.css',
  'radius-mode-1.css',
  'spaces-mode-1.css',
  'sizes-mode-1.css',
  'typography-desktop.css',
]

const lightFiles = ['semantic-color-light.css']
const darkFiles = ['semantic-color-dark.css']

const sections = [
  {
    selector: ':root',
    files: rootFiles,
  },
  {
    selector: ":root,\n[data-theme='light']",
    files: lightFiles,
  },
  {
    selector: "[data-theme='dark']",
    files: darkFiles,
  },
]

const output = [
  '/* Generated by scripts/build-runtime-tokens.cjs.',
  '   Do not edit manually. Source tokens live in src/styles/tokens/. */',
  '',
  ...sections.flatMap(({ selector, files }) => [
    `${selector} {`,
    ...files.flatMap((file) => readTokenFile(file)),
    '}',
    '',
  ]),
].join('\n')

fs.writeFileSync(outputPath, output, 'utf8')
console.log(`Generated ${relative(outputPath)} from ${relative(tokenDir)}/`)

function readTokenFile(fileName) {
  const filePath = path.join(tokenDir, fileName)

  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing token file: ${relative(filePath)}`)
  }

  const lines = fs
    .readFileSync(filePath, 'utf8')
    .split(/\r?\n/)
    .map((line) => line.trimEnd())
    .filter((line) => line.trim().length > 0)

  return [`  /* ${fileName} */`, ...lines.map((line) => `  ${line}`)]
}

function relative(filePath) {
  return path.relative(rootDir, filePath).replaceAll(path.sep, '/')
}
